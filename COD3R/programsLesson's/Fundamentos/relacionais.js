console.log('01)', '1' == 1);
//verdade, porque são valores iguais
//mesmo que sejam de tipos diferentes

//e para verificar valor e tipo?
//ai usa ===
console.log('02)', '2' === 2);
//deu false, porque não é
//o mesmo tipo.

//e o contrario ?
//para ver se é diferente ao inves
//de igual ?
//usa-se o !=
//e se quer ver se são
//estritamente diferentes
//ou seja, em tipos
//usa-se o !==
//igual ==/===, diferente !=/!==

console.log('03)', '3' != 3);

//vai dar false
//porque eles são iguais!
//e repare que não usei ==
//ou seja, só um.
//não vai contar o tipo
//agora, e se contar o tipo?

console.log('04)', '4' !== 4);
//vai dar true
//porque '04' é diferente de 4
//estritamente diferente!
//o valor é o mesmo.
//mas os tipos são diferentes
//o primeiro é string
//o segundo é number
//logo, sim, são diferentes estritamente.

